    private void MostrarCita() throws Exception {
        registrarCitaServicio.MostrarCita(modelo);
    }

    private void estadoCambiado() throws Exception {
        registrarCitaServicio.cambiarEstado(cita);
    }

    private void guardarCita() throws Exception, HeadlessException {
        if (txtEstado.getText().equals("") || cbxServicio.getSelectedItem().equals("") || txtFecha.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "TODOS LOS CAMPOS SON OBLIGATORIOS");
        } else {
            cita.setDetallecita(txtDetallecita.getText());
            registrarCitaServicio.guardarCita(cita);
            estadoCambiado();
            MostrarCita();
            JOptionPane.showMessageDialog(this, "SE GUARDÓ Y SE AGREGÓ LA CITA EXITOSAMENTE", "Información", JOptionPane.INFORMATION_MESSAGE);
        }
    }

    private void capturarDatosDeCita() throws NumberFormatException {
        cita.setFecha(txtFecha.getText().trim());
        cita.setHora(cbxHorario.getSelectedItem().toString());
        cita.setEstado(txtEstado.getText().trim());
        cita.setServicio(cbxServicio.getSelectedItem().toString());
        cita.setDetallecita(txtDetallecita.getText().trim());
        cita.setPagototal(Double.parseDouble(txtPagoTotal.getText().trim()));
    }

    private void activarBotonGuardar() {
        if (cita.getDentista() != null && cita.getPaciente() != null) {
            btnGuardar.setEnabled(true);
            btnCalcularPrecio.setEnabled(true);
            txtPagoTotal.setEditable(false);
        } else {
            btnGuardar.setEnabled(false);
            btnCalcularPrecio.setEnabled(false);
            txtPagoTotal.setEditable(false);
        }
    }

