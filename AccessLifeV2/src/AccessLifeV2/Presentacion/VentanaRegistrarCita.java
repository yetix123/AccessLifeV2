    private void btnBuscarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPacienteActionPerformed
        String dnipacient = txtDniPaciente.getText().trim();
        try {
            paciente = registrarCitaServicio.buscarPaciente(dnipacient);
            cita.setPaciente(paciente);
            txtNombrePaciente.setText(paciente.getNombres());
            txtEdad.setText(String.valueOf(paciente.getEdad()));
            activarBotonGuardar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Advertencia", JOptionPane.WARNING_MESSAGE);
            txtNombrePaciente.setText("");
            txtEdad.setText("");
            txtDniPaciente.requestFocus();
        }
    }//GEN-LAST:event_btnBuscarPacienteActionPerformed

    private void btnBuscarDentistaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarDentistaActionPerformed
        String nombres = txtNomDent.getText().trim();
        try {
            dentista = registrarCitaServicio.buscarDentista(nombres);
            cita.setDentista(dentista);
            txtDniDent.setText(dentista.getDni());
            List<String> horariosDisponibles = registrarCitaServicio.listaDeHoras(dentista);
            horariosDisponibles.add(0, "SELECCIONAR");
            DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>(horariosDisponibles.toArray(new String[0]));
            cbxHorario.setModel(model);
            // Establecer el horario seleccionado como el horario del dentista
            cbxHorario.setSelectedItem(dentista.getHorario());
            cbxHorario.setSelectedIndex(0);
            txtEstado.setText(dentista.getEstado());
            activarBotonGuardar();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Advertencia", JOptionPane.WARNING_MESSAGE);
            txtDniDent.setText("");
            cbxHorario.setSelectedIndex(0);
            txtEstado.setText("");
            txtNomDent.requestFocus();
        }
    }//GEN-LAST:event_btnBuscarDentistaActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            capturarDatosDeCita();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "ERROR AL INGRESAR DATOS", "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            guardarCita();
            inicializarNuevaCita();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Advertencia", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnCalcularPrecioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularPrecioActionPerformed

        int codserv = cbxServicio.getSelectedIndex(); // +1 porque los índices comienzan en 0
        String tiposervicio = cita.TipoServicio(codserv);

        if (tiposervicio != "Seleccionar") {
            String tedad = txtEdad.getText();
            try {
                int edad = Integer.parseInt(tedad);
                double resultado = cita.PagoTotal(edad);
                txtPagoTotal.setText(String.valueOf(resultado));
            } catch (Exception e) {
                e.printStackTrace();
                txtPagoTotal.setText("EDAD NO VALIDA");
            }
        } else {
            txtPagoTotal.setText("Servicio no válido");
        }

    }//GEN-LAST:event_btnCalcularPrecioActionPerformed

